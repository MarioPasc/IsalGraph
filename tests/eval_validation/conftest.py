"""Fixtures for eval_validation tests.

These tests validate IsalGraph properties on real data generated by the
eval_setup pipeline (run on Picasso). They require --eval-data-root pointing
to the pipeline output directory.
"""

from __future__ import annotations

import json
import os
from dataclasses import dataclass, field

import numpy as np
import pytest

DATASETS = ["iam_letter_low", "iam_letter_med", "iam_letter_high", "linux", "aids"]
METHODS = ["exhaustive", "greedy", "greedy_single"]


@dataclass
class DatasetStrings:
    """Canonical strings for one dataset and method."""

    dataset: str
    method: str
    graph_ids: list[str]
    strings: dict[str, str]
    lengths: dict[str, int]
    node_counts: dict[str, int]
    edge_counts: dict[str, int]


@dataclass
class EvalData:
    """All loaded eval data."""

    data_root: str
    canonical: dict[tuple[str, str], DatasetStrings] = field(default_factory=dict)
    ged_matrices: dict[str, np.ndarray] = field(default_factory=dict)
    ged_graph_ids: dict[str, list[str]] = field(default_factory=dict)
    levenshtein_matrices: dict[tuple[str, str], np.ndarray] = field(default_factory=dict)
    wl_distance_matrices: dict[str, np.ndarray] = field(default_factory=dict)
    wl_kernel_matrices: dict[str, np.ndarray] = field(default_factory=dict)


def _load_eval_data(data_root: str) -> EvalData:
    """Load all eval pipeline artifacts."""
    ed = EvalData(data_root=data_root)

    for ds in DATASETS:
        # Load graph metadata
        meta_path = os.path.join(data_root, "graph_metadata", f"{ds}.json")
        if not os.path.exists(meta_path):
            continue
        with open(meta_path) as f:
            meta = json.load(f)

        nc_map = dict(zip(meta["graph_ids"], meta["node_counts"], strict=True))
        ec_map = dict(zip(meta["graph_ids"], meta["edge_counts"], strict=True))

        # Load canonical strings
        for method in METHODS:
            can_path = os.path.join(data_root, "canonical_strings", f"{ds}_{method}.json")
            if not os.path.exists(can_path):
                continue
            with open(can_path) as f:
                can_data = json.load(f)

            strings = {}
            lengths = {}
            for gid, info in can_data["strings"].items():
                strings[gid] = info["string"]
                lengths[gid] = info["length"]

            ed.canonical[(ds, method)] = DatasetStrings(
                dataset=ds,
                method=method,
                graph_ids=list(strings.keys()),
                strings=strings,
                lengths=lengths,
                node_counts={gid: nc_map[gid] for gid in strings if gid in nc_map},
                edge_counts={gid: ec_map[gid] for gid in strings if gid in ec_map},
            )

        # Load GED matrix
        ged_path = os.path.join(data_root, "ged_matrices", f"{ds}.npz")
        if os.path.exists(ged_path):
            ged_data = np.load(ged_path, allow_pickle=True)
            ed.ged_matrices[ds] = ged_data["ged_matrix"]
            ed.ged_graph_ids[ds] = list(ged_data["graph_ids"])

        # Load Levenshtein matrices
        for method in METHODS:
            lev_path = os.path.join(data_root, "levenshtein_matrices", f"{ds}_{method}.npz")
            if os.path.exists(lev_path):
                lev_data = np.load(lev_path, allow_pickle=True)
                ed.levenshtein_matrices[(ds, method)] = lev_data["levenshtein_matrix"]

        # Load WL kernel data
        wl_path = os.path.join(data_root, "wl_kernel_matrices", f"{ds}.npz")
        if os.path.exists(wl_path):
            wl_data = np.load(wl_path, allow_pickle=True)
            ed.wl_distance_matrices[ds] = wl_data["distance_matrix"]
            ed.wl_kernel_matrices[ds] = wl_data["kernel_matrix"]

    return ed


@pytest.fixture(scope="session")
def eval_data_root(request):
    """Return eval data root path, or skip if not provided."""
    root = request.config.getoption("--eval-data-root")
    if root is None:
        pytest.skip("--eval-data-root not provided")
    if not os.path.isdir(root):
        pytest.skip(f"Eval data root not found: {root}")
    return root


@pytest.fixture(scope="session")
def source_dir(request):
    """Return source data directory, or None if not provided."""
    return request.config.getoption("--source-dir")


@pytest.fixture(scope="session")
def eval_data(eval_data_root) -> EvalData:
    """Load all eval data. Cached for the session."""
    return _load_eval_data(eval_data_root)
